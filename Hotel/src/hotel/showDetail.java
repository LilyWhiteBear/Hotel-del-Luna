    public void show_catelogintable()
    {
        ArrayList<Catelog> list = getCatelogList();
        DefaultTableModel model = (DefaultTableModel)Scatelog_T.getModel();
       Scatelog_T.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));
        Object[] row = new Object[5];
        for(int i=0;i<list.size();i++)
        {
            row[0] = list.get(i).getcatelog_id();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getprice();
            row[3] = list.get(i).getmaxguest();
            row[4] = list.get(i).getdetail();

            model.addRow(row);
        }
    }
