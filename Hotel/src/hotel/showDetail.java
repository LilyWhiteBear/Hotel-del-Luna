    public ArrayList<Catelog> getCatelogList()
    {
        ArrayList<Catelog> Cateloglist = new ArrayList<>();
        String sql = "select * from roomcatelog";
        try
        {
            Connection con = connectDB();
            ResultSet rs = con.createStatement().executeQuery(sql);
            Catelog catelog; 
            while(rs.next())
            {
                catelog = new Catelog(rs.getInt("catelog_id"),rs.getString("catelog_name"), rs.getInt("room_price"), rs.getInt("max_guest"), rs.getString("detail"));
                Cateloglist.add(catelog);
            }
        }
        catch(SQLException e)
        {

        }
        return Cateloglist;
    }

