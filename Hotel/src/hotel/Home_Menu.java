    private void Add_room1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_room1ActionPerformed

        if(room_number1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Input Before !\n");
        }
        else
        {
            int roomid = Integer.parseInt(room_number1.getText());
            String catelog = EComboBox_room.getSelectedItem().toString();
            int catelogid = 0;
            try
            {
                Connection con = connectDB();
                String selec = "select catelog_id from roomcatelog where catelog_name = ?";
                p = con.prepareStatement(selec);
                p.setString(1, catelog);
                rs = p.executeQuery();
                if(rs.next())
                {
                    catelogid = rs.getInt("catelog_id");
                }
            }
            catch(SQLException e)
            {
               
            }
            Manager man = new Manager();
            man.addRoom(roomid, catelogid);
            
            DefaultTableModel model = (DefaultTableModel)ETable_roome.getModel();
            model.setRowCount(0);
            show_roomintable();
        }
        
    }//GEN-LAST:event_Add_room1ActionPerformed

