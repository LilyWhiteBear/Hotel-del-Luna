    private void checkOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutActionPerformed
        dd.setRoundingMode(RoundingMode.HALF_UP);
        if(cashInp.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Plese Input Cash");
        }
        else
        {
            if("Calculate".equals(checkOut.getText()))
            {
                double inc = Double.valueOf(cashInp.getText());
                double balance = Double.valueOf(totalPrice.getText());
                if(inc >= balance)
                {
                    changeVal.setText(String.valueOf(dd.format(inc - balance)));
                    checkOut.setText("CheckOut");
                } 
                
            }
            else if("CheckOut".equals(checkOut.getText()))
            {
                String ID = Text_customercheckout.getText();
                double sTotal = Double.valueOf(sumVal.getText());
                double disc;
                if("".equals(discountVal.getText()))
                {
                    disc = 0;
                }
                else
                {
                    Promotion pro = new Promotion();
                    pro.deleteCode(promoCode.getText());
                    disc = Double.valueOf(discountVal.getText());
                }
            double balance = Double.valueOf(totalPrice.getText());
            double cash = Double.valueOf(cashInp.getText());
            Cash c = new Cash(ID, disc, sTotal, balance, cash);
            c.setVisible(true);
            } 
        }
    }//GEN-LAST:event_checkOutActionPerformed

