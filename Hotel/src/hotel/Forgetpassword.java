    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con = connectDB();
        String sql_p = "update staff set password = ? where staff_id = ?";
        String pass1 = new String(Password1.getPassword());
        String pass2 = new String(Password2.getPassword());
        String Textstaff_id = Integer.toString(staff_id);

        if(pass1.isEmpty() && pass2.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Input Before !\n");
        }
        else
        {
            if(pass1.equals(pass2))
            {
                try
                {
                    pst = con.prepareStatement(sql_p);
                    pst.setString(1, pass2);
                    pst.setString(2, Textstaff_id);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Password Changed");
                    new Login_Form().setVisible(true);
                    this.setVisible(false);
                }   
                catch (SQLException e) 
                {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Please Check Password Again");
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

