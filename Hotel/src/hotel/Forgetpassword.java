    private void forgetPass_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgetPass_buttonActionPerformed
        Connection con = connectDB();
        String sql = "select staff_id,email,phone from staff where phone = ? and email = ?";
        
        if(phone_forget.getText().isEmpty() && email_forget.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Input Before !\n");
        }
        else
        {
            try
            {
                pst = con.prepareStatement(sql);
                pst.setString(1, phone_forget.getText());
                pst.setString(2, email_forget.getText());
                rs = pst.executeQuery();

                if(rs.next())
                {
                    staff_id = rs.getInt(1);
                    System.out.println(staff_id);
                        Group_confirm.removeAll();
                        Group_confirm.repaint();
                        Group_confirm.revalidate();

                        Group_confirm.add(password_confirm);
                        Group_confirm.repaint();
                        Group_confirm.revalidate();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Invalid !...\nUsername and Password");
                }
            }   
            catch (SQLException e) 
            {
            JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_forgetPass_buttonActionPerformed

