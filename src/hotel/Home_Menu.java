    private void addmember_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addmember_buttonActionPerformed
        String staff_id = null;
        String sql = "insert into account value(?,?,?,?,?,?,?)";
        
        String passText = new String(jPasswordFieldAdd.getPassword());
        try
        {
            Connection con = connectDB();

            p = con.prepareStatement(sql);
            p.setString(1, staff_id);
            p.setString(2, Text_firstnameAdd.getText());
            p.setString(3, Text_lastnameAdd.getText());
            p.setString(4, Text_usernameAdd.getText());
            p.setString(5, passText);
            p.setString(6, Text_phoneAdd.getText());
            p.setString(7, Text_emailAdd.getText());
            p.execute();
            p.close();
            JOptionPane.showMessageDialog(null, "บันทึกข้อมูลเรียบร้อยแล้ว");
        }
            catch (Exception e)
        {
            e.printStackTrace();
        }

    }//GEN-LAST:event_addmember_buttonActionPerformed

